def search_cities_by_food(food_name: str) -> List[Dict]:
    """
    根据美食名称搜索城市
    
    Args:
        food_name: 美食名称
    
    Returns:
        城市列表，包含匹配度
    """
    if not food_name:
        return []
    
    food_name = food_name.strip()
    results = []
    matched_cities = set()
    
    # 精确匹配
    if food_name in FOOD_TO_CITY:
        for city in FOOD_TO_CITY[food_name]:
            if city not in matched_cities:
                results.append({
                    "city": city,
                    "food": food_name,
                    "match_type": "精确匹配",
                    "confidence": 1.0
                })
                matched_cities.add(city)
    
    # 模糊匹配（包含关系）
    for food, cities in FOOD_TO_CITY.items():
        if food_name in food or food in food_name:
            for city in cities:
                if city not in matched_cities:
                    results.append({
                        "city": city,
                        "food": food,
                        "match_type": "模糊匹配",
                        "confidence": 0.8 if food_name in food else 0.6
                    })
                    matched_cities.add(city)
    
    # 按置信度排序
    results.sort(key=lambda x: x["confidence"], reverse=True)
    
    return results

