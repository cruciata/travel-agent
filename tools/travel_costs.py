# 多城市路线规划
def plan_multi_city_route(cities: List[str], days_per_city: Dict[str, int]) -> Dict:
    """
    规划多城市旅行路线
    使用最近邻算法优化路线
    """
    if len(cities) <= 1:
        return {"route": cities, "total_distance": 0, "flights": []}
    
    # 计算所有城市间的距离矩阵
    distances = {}
    for i, city1 in enumerate(cities):
        for j, city2 in enumerate(cities):
            if i != j:
                distances[(city1, city2)] = calculate_distance(city1, city2)
    
    # 使用贪心算法（最近邻）找到较优路线
    # 从北京或上海开始（如果有的话）
    start_city = None
    for c in ["北京", "上海"]:
        if c in cities:
            start_city = c
            break
    
    if not start_city:
        start_city = cities[0]
    
    # 贪心算法
    route = [start_city]
    remaining = set(cities) - {start_city}
    
    while remaining:
        current = route[-1]
        # 找到最近的未访问城市
        nearest = min(remaining, key=lambda c: distances.get((current, c), float('inf')))
        route.append(nearest)
        remaining.remove(nearest)
    
    # 计算总距离和航班信息
    total_distance = 0
    flights = []
    for i in range(len(route) - 1):
        from_city, to_city = route[i], route[i + 1]
        dist = calculate_distance(from_city, to_city)
        total_distance += dist
        flight = get_flight_price(from_city, to_city)
        flights.append(flight)
    
    return {
        "route": route,
        "total_distance": total_distance,
        "flights": flights,
        "total_flight_cost": sum(f["economy"] for f in flights),
        "strategy": "基于地理位置的最近邻优化算法"
    }

# 根据预算优化路线
def optimize_route_by_budget(cities: List[str], budget: int, days_per_city: Dict[str, int]) -> Dict:
    """根据预算优化多城市路线"""
    
    # 获取基础路线
    route_plan = plan_multi_city_route(cities, days_per_city)
    
    total_cost = route_plan["total_flight_cost"]
    
    # 添加酒店费用
    hotel_costs = {}
    for city in cities:
        nights = days_per_city.get(city, 2)
        hotel = get_hotel_price(city, 4)  # 默认4星
        hotel_costs[city] = hotel["price_per_night"] * nights
        total_cost += hotel_costs[city]
    
    route_plan["hotel_costs"] = hotel_costs
    route_plan["total_hotel_cost"] = sum(hotel_costs.values())
    route_plan["total_cost"] = total_cost
    route_plan["within_budget"] = total_cost <= budget
    route_plan["budget_diff"] = budget - total_cost
    
    # 如果超预算，给出建议
    if not route_plan["within_budget"]:
        route_plan["suggestions"] = [
            "考虑减少一个城市",
            "选择火车代替飞机",
            "选择3星酒店代替4星",
            "缩短行程天数"
        ]
    
    return route_plan
