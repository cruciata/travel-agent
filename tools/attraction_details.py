def format_duration_to_hours(duration_str):
    """将时长字符串转换为小时数"""
    if "小时" in duration_str:
        try:
            return float(duration_str.replace("小时", "").strip())
        except:
            return 2.0
    elif "天" in duration_str:
        try:
            return float(duration_str.replace("天", "").strip()) * 8
        except:
            return 8.0
    return 2.0
